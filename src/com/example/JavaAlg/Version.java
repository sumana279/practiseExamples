package com.example.JavaAlg;

import java.util.Comparator;

public class Version{

	private Integer parentVersion;
	private Integer mainVersion;
	private Integer childVersion;
	private String name;

	public Integer getParentVersion() {
		return parentVersion;
	}

	public void setParentVersion(Integer parentVersion) {
		this.parentVersion = parentVersion;
	}

	public Integer getMainVersion() {
		return mainVersion;
	}

	public void setMainVersion(Integer mainVersion) {
		this.mainVersion = mainVersion;
	}

	public Integer getChildVersion() {
		return childVersion;
	}

	public void setChildVersion(Integer childVersion) {
		this.childVersion = childVersion;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	

}
